package fr.epims.ui.common;

import javax.swing.*;
import javax.swing.border.Border;
import java.awt.*;

/**
 *
 * Framed Panel with a label and an inside component automatically extended along X
 *
 * @author JM235353
 *
 */
public class FramedPanel extends JPanel {

    public FramedPanel(String frameLabel, JComponent component) {
        setLayout(new GridBagLayout());

        Border titledBorder = BorderFactory.createTitledBorder(frameLabel);
        setBorder(titledBorder);

        GridBagConstraints c = new GridBagConstraints();
        c.anchor = GridBagConstraints.NORTHWEST;
        c.fill = GridBagConstraints.BOTH;
        c.insets = new java.awt.Insets(5, 5, 5, 5);

        c.gridx = 0;
        c.gridy = 0;
        c.weightx = 1;
        add(component, c);
    }
}
